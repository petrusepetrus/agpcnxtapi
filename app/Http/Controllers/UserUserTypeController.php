    public function getAvailableUserTypes($user_id)
    {
        $available_user_types = DB::table('user_types')
            ->select('user_type', 'id')
            ->whereNotIn('id', DB::table('user_user_type')
                ->select('user_type_id')
                ->where('user_id', '=', $user_id)->pluck('user_type_id'))
            ->orderBy('user_type')
            ->pluck('user_type', 'id');

        return $available_user_types;
    }
